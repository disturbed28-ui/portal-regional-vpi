-- Corrigir funções de trigger adicionando search_path
CREATE OR REPLACE FUNCTION update_updated_at_justificativas()
RETURNS TRIGGER 
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

